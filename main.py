### Walls ###
wall1 = Wall((3, 3), (4, 4))





while True:

    ### CONTROLLER ###
    keys = pygame.key.get_pressed()

    if keys[pygame.K_ESCAPE]:
        quit()
    for event in pygame.event.get(): # NEED THIS FOR LOOP
        if event.type == pygame.QUIT:
            quit()
    
    # Player Movement #
    if keys[pygame.K_w]:
        player.move(0, player.speed)
    if keys[pygame.K_s]:
        player.move(0, -player.speed)
    if keys[pygame.K_a]:
        player.move(-player.speed, 0)
    if keys[pygame.K_d]:
        player.move(player.speed, 0)

    # Screen Borders #
    if player.left() < 0:
        player.set_left(0)
    elif player.right() > screen.get_width():
        player.set_right(screen.get_width())
    if player.top() < 0:
        player.set_top(0)
    elif player.bottom() > screen.get_height():
        player.set_bottom(screen.get_height())
    






    ### VIEW ###

    # General rules
    screen.fill(grey)
    screen.blit(Player, (player.left(), player.top()))

    wall1.draw(screen)
